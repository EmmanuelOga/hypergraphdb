package org.hypergraphdb.conv.factory;

public class MapMethodDescriptor {

	int keySubstitutionIndex = 0;
	int valueSubstitutionIndex = 1;
	public int getKeySubstitutionIndex() {
		return keySubstitutionIndex;
	}
	public void setKeySubstitutionIndex(int keySubstitutionIndex) {
		this.keySubstitutionIndex = keySubstitutionIndex;
	}
	public int getValueSubstitutionIndex() {
		return valueSubstitutionIndex;
	}
	public void setValueSubstitutionIndex(int valueSubstitutionIndex) {
		this.valueSubstitutionIndex = valueSubstitutionIndex;
	}
}
